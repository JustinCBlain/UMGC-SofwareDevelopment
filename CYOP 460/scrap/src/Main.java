public class Main {
    public static void main(String[] args) {
        double area;
        double base1 = 0;
        double base2 = 4;
        double height = 3;
        area = (base1 + base2) / 2.0 * height;
        System.out.println("A trapezoid with bases of " + base1 + " and " + base2 +
            " and a height of " + height + " has an area of " + area + ".");
    }
}
